/** 카테고리별 아티클 수 */
export const GUIDE_CATEGORY_COUNTS: Record<string, number> = Object.fromEntries(
  GUIDE_CATEGORIES.map((c) => [
    c.key,
    GUIDE_ARTICLES.filter((a) => a.category === c.key).length,
  ]),
)

export function getGuideBySlug(slug: string): GuideArticle | undefined {
  return GUIDE_ARTICLES.find((a) => a.slug === slug)
}

export function getGuidesByCategory(category: string): GuideArticle[] {
  return GUIDE_ARTICLES.filter((a) => a.category === category)
}

export function getRelatedGuides(slug: string, limit = 3): GuideArticle[] {
  const article = getGuideBySlug(slug)
  if (!article) return []
  return GUIDE_ARTICLES.filter(
    (a) => a.slug !== slug && (a.category === article.category || a.tags.some((t) => article.tags.includes(t))),
  ).slice(0, limit)
}
